        'remember_token',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];


    //일대다 관계 설정
    public function boards()
    {
        return $this->hasMany(Board::class, 'email');
    }
